# 执行用时：28 ms, 在所有 Python 提交中击败了76.20% 的用户
# 内存消耗：15.5 MB, 在所有 Python 提交中击败了85.37% 的用户
class Solution(object):
    def maxDepth(self, root):
        """
        :type root: TreeNode
        :rtype: int
        """
        if not root:
            return 0
        return max(self.maxDepth(root.left), self.maxDepth(root.right)) + 1